<script>

    $(document).ready(function() {

        var lastScrollTop = $(window).scrollTop();

        $(window).on('scroll', function() {
            var st = $(this).scrollTop();
            var scrollTop = $(this).scrollTop();
            var windowHeight = $(this).height();
            var documentHeight = $(document).height();
            function toggleLaserVisibility() {
                // Faites apparaître l'image
                $('#ship').removeClass('levitateShip');
                $('#ship').addClass('shipShadow');
                $('.laser').show();
                // Effacez l'image après un court délai d'inactivité
                clearTimeout($.data(this, 'scrollTimer'));
                $.data(this, 'scrollTimer', setTimeout(() => {
                    // Faites disparaître l'image
                    $('#ship').addClass('levitateShip');
                    $('#ship').removeClass('shipShadow');
                    $('.laser').hide();
                }, 200));
            }

            if ((st > lastScrollTop) || (scrollTop === 0)) {
                // Scroll vers le bas
                $('.frontShip').removeClass('rotateShip');
                $('.frontShip').addClass('reverseRotateShip');
                toggleLaserVisibility();
            } else {
                // Scroll vers le haut
                $('.frontShip').removeClass('reverseRotateShip');
                $('.frontShip').addClass('rotateShip');
                toggleLaserVisibility();
            }

            // Si vous êtes tout en bas de la page (avec une petite marge d'erreur)
            if (scrollTop + windowHeight + 1 >= documentHeight) {
                $('.frontShip').removeClass('reverseRotateShip');
                $('.frontShip').addClass('rotateShip');
                toggleLaserVisibility();
            }
            lastScrollTop = st;
        });
    });


    $('.mainProject').mousemove(function(e) {
        var mouseX = e.pageX;
        var mouseY = e.pageY;

        var rotateX = (mouseY / $(this).height() - 0.5) * 2; // Ajustez le coefficient pour la sensibilité
        var rotateY = (mouseX / $(this).width() - 0.5) * 40; // Ajustez le coefficient pour la sensibilité

        $('#ship').css({
            'transform': 'rotateX(' + rotateX + 'deg) rotateY(' + rotateY + 'deg)'
        });
    });

    $('.mainProject').mouseleave(function() {
        // Réinitialise la rotation lorsque la souris quitte la zone de l'image
        $('.ship').css({
            'transform': 'rotateX(0deg) rotateY(0deg)'
        });
    });

</script>